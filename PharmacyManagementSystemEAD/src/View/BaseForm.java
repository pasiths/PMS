    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        if (txtSearch.getText().equals("") || txtSearch.getText().equals(null)) {
            tbload();
        } else {
            try {
                String keyword = txtSearch.getText();
                Connection con = connectionDAL.mycon();
                PreparedStatement ps = con.prepareStatement("SELECT * FROM tbl_categories WHERE status=1 AND id LIKE ? OR status=1 AND title LIKE ? OR status=1 AND description LIKE ?");
                ps.setString(1, "%" + keyword + "%");
                ps.setString(2, "%" + keyword + "%");
                ps.setString(3, "%" + keyword + "%");

                ResultSet rs = ps.executeQuery();
                DefaultTableModel model = (DefaultTableModel) tbl_m.getModel();
                model.setRowCount(0);
                while (rs.next()) {
                    model.addRow(new String[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7)});
                }

            } catch (Exception ex) {
                System.err.println(ex);
            } finally {
                connectionDAL.myconClose();
            }
        }
    }//GEN-LAST:event_txtSearchKeyReleased

